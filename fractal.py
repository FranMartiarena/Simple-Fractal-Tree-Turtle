def arbol(tam, prof, color):
    if prof == 0:
        return
    else:
        turtle.color(random.choice(color_list))
        turtle.forward(tam)
        turtle.left(45)
        arbol(tam/1.5, prof-1, color)
        turtle.right(90)
        arbol(tam/1.5, prof-1, color)
        turtle.left(45)
        turtle.back(tam)

turtle.pensize(1)
turtle.hideturtle()
turtle.setup(width=1000,height=900,startx=500,starty=100)
turtle.color("yellow")
turtle.bgcolor("black")
turtle.speed(1000)
turtle.left(90)
for x in range(4):
    arbol(150, 10, color_list)
    turtle.right(90)
turtle.done()
